  // Intel Desk (Session 5.2) — spy network for rival intelligence.
  intel: IntelState;
  // Newspaper (Session 5.3) — press coverage and brand heat.
  newspaper: NewspaperState;
  // Notification Engine (Session 5.4) — persistent prioritised alert log.
