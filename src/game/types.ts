// ============================================================================

export type AideDomain = 'legal' | 'pr' | 'finance' | 'tech' | 'logistics' | 'creative';

export interface AideState {
  id: string;
  loyalty: number;             // 0–100
  lastBriefAt: number;         // ms — gates Brief button (120 s cooldown)
  deployCooldownUntil: number; // ms — gates Deploy button (600 s cooldown)
}

// ============================================================================
// Companion system (Session 3.4) — mirror of the rival system.
// Where rivals have aggression that rises and strikes, companions have trust
// that rises and gives.
// ============================================================================

export type TrustRung =
  | 'ACQUAINTANCE' // just joined — words and small help
  | 'COLLEAGUE'    // reliable — steady passive benefit
  | 'CONFIDANT'    // trusted — active counsel and warnings
  | 'INNER_CIRCLE' // deeply loyal — signature ability, public defence
  | 'LEGACY'       // terminal positive — survives prestige resets
  | 'ESTRANGED';   // terminal negative — betrayed past recovery

/** A supportive action the companion takes based on current need. */
export interface CompanionMove {
  kind:
    | 'counsel'       // warns before a costly mistake
    | 'cover'         // absorbs one rival hit
    | 'boost'         // passive production multiplier
    | 'rally'         // temporary surge in a crisis
    | 'loyalty_save'  // refuses a rival's poach attempt
    | 'signature';    // standout active power
  message: string;
  /** Multiplier applied (for boost/rally/signature). */
  mult?: number;
  /** Duration in seconds (for temporary effects). */
  durationSec?: number;
}

export interface CompanionState {
  id: string;
  trust: number;     // 0–100
  rung: TrustRung;
  lastActionAt: number;
  cooldownUntil: number;
  pendingTrustDelta: number;
  /** Accumulated seconds at Inner Circle+ (drives signature ability growth). */
  loyaltyHeldSec: number;
}

// ============================================================================
// Rival system (Session 3.2)
// ============================================================================

export type RivalPosture =
  | 'DORMANT'
  | 'WATCHING'
  | 'PROVOKED'
  | 'HOSTILE'
  | 'WAR'
  | 'DEFEATED'
  | 'ALLIED';

/** A move the rival has announced but not yet executed. Player has a window to counter. */
export interface RivalTelegraph {
  rivalId: string;
  moveId: string;
  message: string;
  executesAt: number; // ms timestamp
  /** Action type the player dispatches to counter, or null if uncounterable. */
  counteredBy: string | null;
  counterLabel: string | null;
}

/** A negative economic effect currently active from a rival strike. */
export interface RivalPressure {
  rivalId: string;
  kind: 'price' | 'production' | 'brand';
  /** Multiplicative modifier applied to the relevant multiplier (e.g. 0.75 = −25%). */
  multiplier: number;
  endsAt: number; // ms timestamp
}

export interface RivalState {
  id: string;
  aggression: number;    // 0–100
  relationship: number;  // −100 (war) to +100 (allied)
  posture: RivalPosture;
  telegraph: RivalTelegraph | null;
  lastMoveAt: number;    // ms timestamp
  cooldownUntil: number; // ms timestamp
  pendingAggDelta: number;
  // Session 3.3 additions.
  /** Last 5 move IDs the player countered, oldest first. Used for counter-adaptation. */
  defenseHistory: string[];
  /** True when this telegraph is a feint (no effect on execution). */
  telegraphIsFeint: boolean;
  /** How many times the player has directly attacked this rival. */
  timesAttacked: number;
}

// ============================================================================
// Dynasty system (Session 4.5) — prestige chain, earned traits, emergent beats.
// Tracks outcomes of each prestige run; traits persist and compound across
// generations; emergent beats are procedurally generated from dynasty history.
// ============================================================================

export interface DynastyTrait {
  id: string;
  label: string;
  desc: string;
  /** Multiplicative bonus to production (e.g. 1.05 = +5%). */
  prodMult: number;
  /** Fraction cost discount (e.g. 0.03 = 3% off). Additive across traits, capped in engine. */
  costDiscount: number;
}

export interface DynastyRun {
  /** 0-based: 0 = first run (before first prestige). */
  runIdx: number;
  endEarnings: number;
  finalEthics: number;
  /** Companion IDs at LEGACY rung when prestige fired. */
  companionsLegacy: string[];
  /** Rival IDs that reached DEFEATED posture during the run. */
  rivalsBested: string[];
  /** Premise clauses fulfilled at time of prestige. */
  clausesFulfilled: number;
  /** Trait ID earned this run, or null. */
  traitEarned: string | null;
}

export interface DynastyState {
  /** Completed runs, oldest first. */
  runs: DynastyRun[];
  /** Active traits from past runs (at most one new trait per run). */
  traits: DynastyTrait[];
  /** Cosmetic heirloom name strings unlocked at prestige milestones. */
  heirlooms: string[];
}

// ============================================================================
// Echelon system (Session 5.1) — competitive ranking ladder.
// Tier advances as lifetime earnings grow; each tier grants a passive
// production multiplier. Rising in the world attracts press coverage and
// intensifies rival aggression.
// ============================================================================

export type EchelonTier =
  | 'STARTUP'    // < $10K lifetime earnings
  | 'CONTENDER'  // $10K – $1M
  | 'PLAYER'     // $1M – $100M
  | 'LEADER'     // $100M – $10B
  | 'MOGUL'      // $10B – $1T
  | 'TITAN';     // $1T+

export interface EchelonState {
  tier: EchelonTier;
  /** 0–100 progress within current tier. */
  points: number;
  /** ms timestamp when tier last advanced (0 = never). */
  lastAdvancedAt: number;
}

// ============================================================================
// Intel Desk (Session 5.2) — spy network for rival intelligence.
// Commissioning a report investigates whether an active rival telegraph is
// a feint. Reports resolve after 30 s; intel level decays slowly over time.
// ============================================================================

export interface IntelReport {
  rivalId: string;
  commissionedAt: number; // ms
  /** ms — resolves 30 s after commissioned. */
  revealsAt: number;
  resolved: boolean;
  /** null until resolved; true = the current telegraph is a feint. */
  wasFeint: boolean | null;
}

export interface IntelState {
  /** 0–100 network strength. Gained by commissioning reports; decays 2/min. */
  level: number;
  /** Up to 5 most-recent reports. */
  reports: IntelReport[];
  /** ms — gates "Commission" button (300 s cooldown). */
  lastBriefAt: number;
}

// ============================================================================
// Newspaper (Session 5.3) — press coverage system.
// Notable transitions trigger headlines; negative coverage accumulates a heat
// score that applies a small production debuff until the player responds.
// ============================================================================

export type NewsCategory = 'business' | 'scandal' | 'politics' | 'victory' | 'market';

export interface NewsItem {
  id: string;
  at: number;             // ms timestamp
  category: NewsCategory;
  headline: string;
  body: string;
  /** −10 (crisis) to +10 (triumph). */
  sentimentScore: number;
  read: boolean;
  responded: boolean;
}

export interface NewspaperState {
  /** Rolling archive — max 20 items, newest first. */
  items: NewsItem[];
  /** ms — gates new items (min 60 s apart). */
  lastPublishedAt: number;
  /** Accumulated negative coverage (0–100). Decays 0.5/min; respond to reduce. */
  heatScore: number;
}

// ============================================================================
