// ============================================================================
// Premise system (Session 4.3) — Old Master's inheritance with named clauses.
// Each clause evaluates a game-state condition; once sustained long enough it
// locks in a production/cost bonus. Breaching a fulfilled clause suspends the
// bonus until the condition is re-earned.
// ============================================================================

export type ClauseStatus = 'locked' | 'progressing' | 'fulfilled' | 'breached';

export interface PremiseClause {
  id: string;
  status: ClauseStatus;
  /** Seconds the condition has been continuously met (resets on break). */
  holdSec: number;
  /** ms timestamp when first fulfilled; 0 if never. */
  fulfilledAt: number;
  /** Seconds the condition has been broken while in the grace window. */
  breachSec: number;
}

export interface PremiseState {
  /** ms timestamp when the will was first revealed to the player. */
  revealedAt: number;
  clauses: PremiseClause[];
}

